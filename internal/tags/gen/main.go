// convertPath converts a fully-qualified Protobuf path from descriptor.proto
// into a nice variable name.
func convertPath(name protoreflect.FullName) string {
	path := string(name)
	path = strings.TrimPrefix(path, "google.protobuf.")

	buf := new(strings.Builder)
	prev := ""
	for c := range strings.SplitSeq(path, ".") {
		if c == "VisibilityFeature" {
			continue
		}

		if c == "DescriptorProto" {
			c = "Message"
		}
		c, _ = strings.CutSuffix(c, "DescriptorProto")
		c = cases.Pascal.Convert(c)
		if _, _, c2 := stringsx.CutOverlap(prev, c); c2 != "" {
			c = c2
		}

		if buf.Len() > 0 {
			buf.WriteByte('_')
		}
		buf.WriteString(c)
		prev = c
	}

	return buf.String()
}

func walk[D protoreflect.Descriptor](d protoreflect.FileDescriptor) iter.Seq[D] {
	return func(y func(D) bool) {
		yield := func(d protoreflect.Descriptor) bool {
			v, ok := d.(D)
			return !ok || y(v)
		}

		var rec func(protoreflect.Descriptor) bool
		rec = func(d protoreflect.Descriptor) bool {
			switch d := d.(type) {
			case protoreflect.FileDescriptor:
				return yield(d) && each(d.Enums(), rec) && each(d.Messages(), rec)
			case protoreflect.MessageDescriptor:
				return yield(d) && each(d.Enums(), rec) &&
					each(d.Fields(), yield) &&
					each(d.Messages(), rec)
			case protoreflect.EnumDescriptor:
				return yield(d) && each(d.Values(), rec)
			default:
				return yield(d)
			}
		}

		rec(d)
	}
}

func each[T protoreflect.Descriptor, L interface {
	Len() int
	Get(int) T
}](l L, yield func(protoreflect.Descriptor) bool) bool {
	for i := range l.Len() {
		if !yield(l.Get(i)) {
			return false
		}
	}
	return true
}

func main() {
	flagx.Main(func() error {
		fdp := descriptorpb.File_google_protobuf_descriptor_proto
		anyfdp := anypb.File_google_protobuf_any_proto
		info, ok := debug.ReadBuildInfo()
		if !ok {
			return errors.New("debug: could not read build info")
		}

		out := new(bytes.Buffer)
		fmt.Fprintf(out, "// Code generated by %s. DO NOT EDIT.\n\n", info.Path)
		fmt.Fprintf(out, "package %s\n\n", os.Getenv("GOPACKAGE"))

		fmt.Fprintf(out, "const (\n")
		for field := range walk[protoreflect.FieldDescriptor](fdp) {
			fmt.Fprintf(out, "// Field number for %v.\n", field.FullName())
			fmt.Fprintf(out, "\t%s = %v\n\n", convertPath(field.FullName()), field.Number())
		}
		for field := range walk[protoreflect.FieldDescriptor](anyfdp) {
			fmt.Fprintf(out, "// Field number for %v.\n", field.FullName())
			fmt.Fprintf(out, "\t%s = %v\n\n", convertPath(field.FullName()), field.Number())
		}
		fmt.Fprintf(out, ")\n\n")

		fmt.Fprintf(out, "const (\n")
		for value := range walk[protoreflect.EnumValueDescriptor](fdp) {
			fmt.Fprintf(out, "// Enum value for %v.\n", value.FullName())

			// Use nice, enum-relative names.
			name := value.Parent().FullName() + "." + protoreflect.FullName(value.Name())
			fmt.Fprintf(out, "\t%s = %v\n\n", convertPath(name), value.Number())
		}
		fmt.Fprintf(out, ")\n\n")

		src, err := format.Source(out.Bytes())
		if err != nil {
			return err
		}

		return os.WriteFile("generated.go", src, 0666) //nolint:gosec
	})
}
